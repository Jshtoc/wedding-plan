/**
 * Look up LAWD_CD from Naver Geocoding's city + district names.
 * Falls back to fuzzy matching (contains) if exact match fails.
 *
 * @param city    시도 shortName from Naver (e.g. "서울", "경기")
 * @param district 시군구 shortName (e.g. "강남구", "분당구")
 * @returns 5-digit LAWD_CD or empty string
 */
export function findLawdCd(city: string, district: string): string {
  if (!city || !district) return "";

  // Expand short city names to full
  const fullCity = expandCity(city);
  const key = `${fullCity} ${district}`;

  // Exact match
  if (LAWD_CODES[key]) return LAWD_CODES[key];

  // Fuzzy — find key that ends with the district
  for (const [k, v] of Object.entries(LAWD_CODES)) {
    if (k.startsWith(fullCity) && k.includes(district)) return v;
  }

  // Try without spaces in district (경기도 시군구 합체)
  const noSpace = district.replace(/\s/g, "");
  for (const [k, v] of Object.entries(LAWD_CODES)) {
    if (k.startsWith(fullCity) && k.replace(/\s/g, "").includes(noSpace))
      return v;
  }

  return "";
}

